#!/usr/bin/env node

import { promises as fs } from "node:fs"
import os from "node:os"
import path from "node:path"

const home = os.homedir()
const opencodeRoot = path.join(home, ".config", "opencode")
const registryPath = path.join(opencodeRoot, "skill-registry.md")

const skillSources = [
  { id: "opencode-skill", dir: path.join(opencodeRoot, "skill"), priority: 1 },
  { id: "opencode-skills", dir: path.join(opencodeRoot, "skills"), priority: 2 },
  { id: "agents-shared", dir: path.join(home, ".agents", "skills"), priority: 3 },
]

const ignoredDirs = new Set([".git", "node_modules", ".DS_Store"])

function normalizePath(filePath) {
  return filePath.replace(home, "~")
}

function extractFrontmatter(raw) {
  const match = raw.match(/^---\n([\s\S]*?)\n---/)
  return match ? match[1] : ""
}

function extractValue(frontmatter, key) {
  const regex = new RegExp(`^${key}:\\s*(.+)$`, "m")
  const match = frontmatter.match(regex)
  return match ? match[1].trim().replace(/^['"]|['"]$/g, "") : ""
}

function extractTrigger(raw) {
  const triggerMatch = raw.match(/Trigger:\s*([^\n]+)/i)
  return triggerMatch ? triggerMatch[1].trim() : "Contextual skill loading"
}

async function dirExists(dirPath) {
  try {
    const stat = await fs.stat(dirPath)
    return stat.isDirectory()
  } catch {
    return false
  }
}

async function walkForSkillFiles(baseDir) {
  const files = []

  async function walk(currentDir) {
    let entries
    try {
      entries = await fs.readdir(currentDir, { withFileTypes: true })
    } catch {
      return
    }

    for (const entry of entries) {
      const entryPath = path.join(currentDir, entry.name)
      if (entry.isDirectory()) {
        if (ignoredDirs.has(entry.name)) continue
        await walk(entryPath)
        continue
      }

      if (entry.isFile() && entry.name === "SKILL.md") {
        files.push(entryPath)
      }
    }
  }

  await walk(baseDir)
  return files
}

async function collectSkills() {
  const pickedByName = new Map()
  const scannedSources = []

  for (const source of skillSources) {
    if (!(await dirExists(source.dir))) continue

    scannedSources.push({ id: source.id, dir: source.dir })
    const skillFiles = await walkForSkillFiles(source.dir)

    for (const skillFile of skillFiles) {
      let raw
      try {
        raw = await fs.readFile(skillFile, "utf8")
      } catch {
        continue
      }

      const frontmatter = extractFrontmatter(raw)
      const fallbackName = path.basename(path.dirname(skillFile)).toLowerCase()
      const name = (extractValue(frontmatter, "name") || fallbackName).trim()
      const trigger = extractTrigger(raw)

      const candidate = {
        name,
        trigger,
        path: normalizePath(skillFile),
        source: source.id,
        priority: source.priority,
      }

      const existing = pickedByName.get(name)
      if (!existing || candidate.priority < existing.priority) {
        pickedByName.set(name, candidate)
      }
    }
  }

  const skills = [...pickedByName.values()].sort((a, b) => a.name.localeCompare(b.name))
  return { skills, scannedSources }
}

function buildMarkdown({ skills, scannedSources }) {
  const generatedAt = new Date().toISOString()
  const sourceLines = scannedSources
    .map((s) => `- \`${normalizePath(s.dir)}\` (${s.id})`)
    .join("\n")

  const rows = skills
    .map((skill) => {
      return `| ${skill.name} | ${skill.trigger} | \`${skill.path}\` | ${skill.source} |`
    })
    .join("\n")

  return `# Skill Registry

Central catalog of installed skills for OpenCode.

**Auto-generated by**: \`scripts/update-skill-registry.mjs\`
**Last updated**: ${generatedAt}
**Total skills**: ${skills.length}

---

## Scanned sources

${sourceLines || "- _No skill sources found_"}

---

## Available skills

| Skill | Trigger | Path | Source |
|---|---|---|---|
${rows || "| _none_ | No skills detected | _none_ | _none_ |"}

---

## Quick stack hints

| Detected file or dependency | Recommended skills |
|---|---|
| \`package.json\` + React | react-19, typescript, tailwind-4 |
| \`package.json\` + Next.js | nextjs-15, react-19, typescript, tailwind-4 |
| \`@tanstack/react-query\` | tanstack-query-best-practices |
| \`composer.json\` + Laravel | laravel-inertia-react |
| \`requirements.txt\` or \`pyproject.toml\` + Django | django-drf |
| \`playwright.config.*\` | playwright |
| \`pytest.ini\` or \`conftest.py\` | pytest |
| \`tsconfig.json\` | typescript |

---

## Notes

- Do not edit this file manually.
- Regenerate after adding/removing/updating skills.
`
}

async function main() {
  const result = await collectSkills()
  const markdown = buildMarkdown(result)
  await fs.writeFile(registryPath, markdown, "utf8")

  process.stdout.write(`Skill registry updated: ${registryPath}\n`)
  process.stdout.write(`Skills detected: ${result.skills.length}\n`)
}

main().catch((error) => {
  process.stderr.write(`Error updating skill registry: ${error.message}\n`)
  process.exit(1)
})
